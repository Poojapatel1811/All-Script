package Day2;

public class Demo3 {
	static int p=2;
	static int q=4;
	
	public static void main(String[] args) {
		System.out.println(p & q);
		System.out.println(p | q);
		System.out.println(p ^ q);
		System.out.println(~q);
		System.out.println(p<<2);
		System.out.println(p>>2);
		System.out.println(p>>>2);
		
	}

}
