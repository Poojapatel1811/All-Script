package Day2;

public class Demo {
	int i;
	char c;
	boolean b;
	byte b1;
	short s;
	long l;
	float f;
	double d1;

	public void Datatype()
	{
		
		System.out.println("Bydefault Int value "+i);
		System.out.println("Bydefault Char value "+c);
		System.out.println("Bydefault boolean value "+b);
		System.out.println("Bydefault byte value "+b1);
		System.out.println("Bydefault short value "+s);
		System.out.println("Bydefault long value "+l);
		System.out.println("Bydefault float value "+f);
		System.out.println("Bydefault double value "+d1);
	}

	public static void main(String[] args) {
		Demo d=new Demo();
		d.Datatype();
		
	}
}
