package Day3;

public class BreakContinue {
	   public static void main(String args[]){
	      int a =0;
	   for(a=0;a<8;a++)
	      {
	          if (a==4)
	          {
	             break;
	          }
	         
	      System.out.println(a);
	      }
	      
	   	for(a=0;a<8;a++)
	      {
	          if (a==5)
	          {
	             continue;
	          }
	     
	      System.out.println(a); 
	   
	  }   
	   
	   }
}
	